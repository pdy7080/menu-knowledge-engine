def main():
    """CSV 파일 생성"""
    menus = generate_menu_names()

    output_file = "C:/project/menu/app/data/real_menu_names_300.csv"

    with open(output_file, 'w', encoding='utf-8-sig', newline='') as f:
        writer = csv.writer(f)
        # 헤더 작성
        writer.writerow(['menu_name_ko', 'expected_concept', 'frequency_tier', 'has_modifier', 'notes'])
        # 데이터 작성
        for menu in menus:
            writer.writerow(menu)

    print(f"[OK] CSV file created: {output_file}")
    print(f"[OK] Total menu names: {len(menus)}")

    # 통계 출력
    tier_counts = {}
    modifier_counts = {'Y': 0, 'N': 0}
    for menu in menus:
        tier = menu[2]
        has_mod = menu[3]
        tier_counts[tier] = tier_counts.get(tier, 0) + 1
        modifier_counts[has_mod] += 1

    print()
    print("=== Statistics ===")
    print(f"빈도 상: {tier_counts.get('상', 0)}개")
    print(f"빈도 중: {tier_counts.get('중', 0)}개")
    print(f"빈도 하: {tier_counts.get('하', 0)}개")
    print()
    print(f"수식어 포함 (Y): {modifier_counts['Y']}개")
    print(f"수식어 없음 (N): {modifier_counts['N']}개")


if __name__ == "__main__":
    main()
